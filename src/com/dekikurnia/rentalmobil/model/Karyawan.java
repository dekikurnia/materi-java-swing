package com.dekikurnia.rentalmobil.model;

/**
 *
 * @author deki kurnia
 */
public class Karyawan {
    
    private String nik;
    private String namaKaryawan;    
    private String alamatKaryawan;
    private String telpKaryawan;

    /**
     * @return the nik
     */
    public String getNik() {
        return nik;
    }

    /**
     * @param nik the nik to set
     */
    public void setNik(String nik) {
        this.nik = nik;
    }

    /**
     * @return the namaKaryawan
     */
    public String getNamaKaryawan() {
        return namaKaryawan;
    }

    /**
     * @param namaKaryawan the namaKaryawan to set
     */
    public void setNamaKaryawan(String namaKaryawan) {
        this.namaKaryawan = namaKaryawan;
    }

    /**
     * @return the alamatKaryawan
     */
    public String getAlamatKaryawan() {
        return alamatKaryawan;
    }

    /**
     * @param alamatKaryawan the alamatKaryawan to set
     */
    public void setAlamatKaryawan(String alamatKaryawan) {
        this.alamatKaryawan = alamatKaryawan;
    }

    /**
     * @return the telpKaryawan
     */
    public String getTelpKaryawan() {
        return telpKaryawan;
    }

    /**
     * @param telpKaryawan the telpKaryawan to set
     */
    public void setTelpKaryawan(String telpKaryawan) {
        this.telpKaryawan = telpKaryawan;
    }
    
    
    
}
